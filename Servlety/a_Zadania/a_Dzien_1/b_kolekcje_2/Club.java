package a_Zadania.a_Dzien_1.b_kolekcje_2;

public class Club {
    String name;
    String city;

    public Club(String name, String city) {
        this.name = name;
        this.city = city;
    }

    public String getName() {
        return name;
    }

    public String getCity() {
        return city;
    }
}
